/**
 * 日付文字列 (YYYY-MM-DD) から祝日名を取得する。
 */
export function getHolidayName(dateStr: string): string | null {
    return HOLIDAYS_JP[dateStr] || null;
}

/**
 * 日付文字列 (YYYY-MM-DD) が祝日（または土日）かどうかを判定する。
 */
export function isOffDay(dateStr: string): boolean {
    // 祝日チェック
    if (HOLIDAYS_JP[dateStr]) return true;

    // 土日チェック
    const parts = dateStr.split("-");
    if (parts.length !== 3) return false;
    const date = new Date(Number(parts[0]), Number(parts[1]) - 1, Number(parts[2]));
    const day = date.getDay();
    return day === 0 || day === 6; // 0:日, 6:土
}
